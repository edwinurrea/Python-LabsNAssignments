def my_max(num_one, num_two) :
    if num_one > num_two :
        return num_one
    else :
        return num_two

def my_min(num_one, num_two) :
    if num_one < num_two :
        return num_one
    else :
        return num_two

def my_avg(num_one, num_two) :
    avg_num = float(num_one + num_two) / 2
    return avg_num
